    private void setOther(){
        otherUser.removeAll();
        for(User u : user.allUser()){
            otherUser.add(new OtherUserP(this.user, u){
                @Override
                public void removeMe(){
                    setOther();
                }
            });
        }
        int r = user.allUser().size();
        r = r/2 + r%2;
        otherUser.setLayout(new GridLayout(r,2));
    }

    
    private void setData(){
        name.setText(user.getName());
        
        lvl.setText(user.getLevel() + "");
        dateOfB.setText(user.getDateOfBirth());
        userName.setText(user.getUsername());
        if(user.getGender()!= null){
            genderCB.setSelectedItem(user.getGender());
        }else{
            genderCB.setSelectedItem(0);
        }
        ImageIcon mg = new ImageIcon(user.getImageDIR());
        Image a = mg.getImage().getScaledInstance(imgsz, imgsz, Image.SCALE_SMOOTH);
        mg = new ImageIcon(a);
        img.setIcon(mg);
    }
    
    private void setMode(){
        name.setEnabled(mode == 1);
        dateOfB.setEnabled(mode == 1);
        userName.setEditable(false);
        genderCB.setEnabled(mode == 1);
        saveBt.setVisible(mode == 1);
        imgBt.setVisible(mode == 1);
        EditBt.setVisible(mode != 1);
        
    }
    
    private void getData(){
        User nu = new User(user.getUsername(), user.getPassword());
        if(nu.getUser()){
            nu.setName(name.getText());
            nu.setGender(genderCB.getSelectedItem().toString());
            nu.setDateOfBirth(dateOfB.getText());
            nu.setImageDIR(user.getImageDIR().replaceAll("\\\\", "\\\\\\\\"));
            user.updateInfo(nu);
        }
    }
