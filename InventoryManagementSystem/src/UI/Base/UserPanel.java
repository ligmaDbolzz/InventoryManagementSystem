    private void resetPassBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetPassBtActionPerformed
        try {
            String np = JOptionPane.showInputDialog("Enter new password.");
            if(np != null){
                User npu = new User(user.getUsername(), new HashPassword().hashPassword(np));
                this.user.updateInfo(npu);
                this.mode = 0;
                setData();
                setMode();
            }
        } catch (NoSuchAlgorithmException ex) {
            JOptionPane.showMessageDialog(null, "Unable to reset password");
            Logger.getLogger(UserPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_resetPassBtActionPerformed

    private void imgBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_imgBtActionPerformed
        int returnVal = imgChoser.showOpenDialog(this);
        if(returnVal == JFileChooser.APPROVE_OPTION){
            File imgFile = imgChoser.getSelectedFile();
            String now = imgFile.getAbsolutePath();
            user.setImageDIR(now);
            
            ImageIcon mg = new ImageIcon(user.getImageDIR());
            Image a = mg.getImage().getScaledInstance(imgsz, imgsz, Image.SCALE_SMOOTH);
            mg = new ImageIcon(a);
            img.setIcon(mg);
        }
    }//GEN-LAST:event_imgBtActionPerformed


